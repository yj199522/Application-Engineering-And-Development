    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(phoneText.getText().isEmpty() || nameText.getText().isEmpty() || locationText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,"Fields cannot be empty", "Error message", JOptionPane.ERROR_MESSAGE);
            return;
        }else if(phoneText.getText().length() != 10) {
            JOptionPane.showMessageDialog(null, "PhoneNumber must be of 10 digits", "Error message", JOptionPane.ERROR_MESSAGE);
            return;
        }else if(!phoneText.getText().matches("^[0-9]+$")) {
            JOptionPane.showMessageDialog(null, "Phone Number must have digits only", "Error message", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            restaurant.setName(nameText.getText());        
            restaurant.setLocation(locationText.getText());
            restaurant.setPhone(phoneText.getText());
            JOptionPane.showMessageDialog(null, "Restaurant updated successfully");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


