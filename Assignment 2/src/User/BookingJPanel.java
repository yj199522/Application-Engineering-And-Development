    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int rowIndex = jTable1.getSelectedRow();
        if(rowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please Select Row from the table to delete", "Error Message",JOptionPane.ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Car selectedRow = (Car)model.getValueAt(rowIndex, 0);
        JOptionPane.showMessageDialog(this, "Data Deleted Successfully");
        DateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy HH:mm:ss");  
        Date today = Calendar.getInstance().getTime();  
        history.setUpdateDate(dateFormat.format(today));
        selectedRow.setAvaliable("Yes");
        int index = 0;
        for(Car i: history.getHistory()){
            if(i.getSerialNo() == selectedRow.getSerialNo()) break;
            ++index;
        }
        bookHistory.deleteData(selectedRow);
        history.updateData(index, selectedRow);
        populateTable();
    }//GEN-LAST:event_DeleteActionPerformed

