    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        String fname = FNameTxt.getText();
        String lname = LNameTxt.getText();
        String personId = PersonIdTxt.getText();
        Double age = 0d;
        String address = AddressTxt.getText();
        String city = CityTxt.getText();
        Boolean isVaild = true;
        int tele = ContactTxt.getText().trim().length();
       
        for(Person i : personHistory.getPersonHistory()){
            if(i.getPersonId().equals(personId)){
                JOptionPane.showMessageDialog(this, "Person Id already present it should be unique", "Error message", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        for(Patient i : patientHistory.getPatientHistory()){
            if(i.getPersonId().equals(personId)){
                JOptionPane.showMessageDialog(this, "Person Id already present it should be unique", "Error message", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        if(fname.trim().isEmpty()) {
            FNameErr.setText("First Name is required");
            isVaild = false;
        }
        else FNameErr.setText("");
        
         if(tele > 0) {
            try {
            long d = Long.parseLong(ContactTxt.getText());
                if (tele < 10 || tele > 10) {
                    ContactErr.setText("Telephone numbers is wrong");
                    isVaild = false;
                } else{
                    ContactErr.setText("");
                }
            } catch (NumberFormatException nfe) {
                ContactErr.setText("Telephone can only contain numbers");
                isVaild = false;
            }
        }else {
             ContactErr.setText("Telephone is required");
             isVaild = false;
         }
         
        if(lname.trim().isEmpty()) {
            LNameErr.setText("Last Name is required");
            isVaild = false;
        }
        else LNameErr.setText("");
        
        if(personId.trim().isEmpty()) {
            PersonIdErr.setText("PersonId is required");
            isVaild = false;
        }
        else PersonIdErr.setText("");
        
        if(city.trim().isEmpty()) {
            CityErr.setText("City is required");
            isVaild = false;
        }
        else CityErr.setText("");
        
        if(address.trim().isEmpty()) {
            AddressErr.setText("Model Number is required");
            isVaild = false;
        }
        else AddressErr.setText("");
        
        if(!AgeTxt.getText().trim().isEmpty()){
        try {
            age = Double.parseDouble(AgeTxt.getText());
            AgeErr.setText("");
            } catch (NumberFormatException nfe) {
                AgeErr.setText("Age must be number");
                isVaild = false;
            }
         }else{
            AgeErr.setText("Age is required");
            isVaild = false;
        }
        
        if(isVaild){
             if (age <= 1) {
                ageGroup = "newBorn";
            } else if (age >= 0 && age <=1) {
                ageGroup = "infant";
            } else if (age > 1 && age <= 3) {
                ageGroup = "toddler";
            } else if (age > 3 && age <= 5) {
                ageGroup = "preschool";
            } else if (age > 5 && age <= 12) {
                ageGroup = "school";
            } else if (age >= 13) {
                ageGroup = "adolescent";
            }
            Patient person = patientHistory.addPatientData();
            person.setPersonId(personId);
            person.setFirstName(fname);
            person.setLastName(lname);
            person.setAge(age);
            person.setCity(city);
            person.setHouse(address);
            person.setSex(sex);
            person.setAgeType(ageGroup);
            person.setCommunity(communityGroup);
            person.setContact(Long.parseLong(ContactTxt.getText()));
            person.setType("Patient");
            
            Person person1 = personHistory.addPersonData();
            person1.setPersonId(personId);
            person1.setFirstName(fname);
            person1.setLastName(lname);
            person1.setAge(age);
            person1.setCity(city);
            person1.setHouse(address);
            person1.setSex(sex);
            person1.setAgeType(ageGroup);
            person1.setCommunity(communityGroup);
            person1.setContact(Long.parseLong(ContactTxt.getText()));
            person1.setType("Patient");
            
            JOptionPane.showMessageDialog(this, "Data Added Successfully");  

            FNameTxt.setText("");
            LNameTxt.setText("");
            PersonIdTxt.setText("");
            AgeTxt.setText("");
            AddressTxt.setText("");
            CityTxt.setText("");
            ContactTxt.setText("");
            Test1.setSelected(true);
            Test2.setSelected(false);
            Test3.setSelected(false);
            Male.setSelected(true);
            Female.setSelected(false);
        }
    }//GEN-LAST:event_SubmitActionPerformed

