    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int rowIndex = jTable1.getSelectedRow();
        if(rowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please Select Row from the table to delete", "Error Message",JOptionPane.ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Patient selectedRow = (Patient)model.getValueAt(rowIndex, 0);
        JOptionPane.showMessageDialog(this, "Data Deleted Successfully");
        rowIndex = 0;
        Person person = null;
             for(Person i: personHistory.getPersonHistory()){
                if(selectedRow.getPersonId() == i.getPersonId()){
                    person = i;
                    break;
                }
                rowIndex++;
            }
        personHistory.deleteData(person);
        patientHistory.deleteData(selectedRow);
        SearchActionPerformed(evt);
    }//GEN-LAST:event_DeleteActionPerformed

