    private void filterData() {
        filterList = patientHistory.getPatientHistory();
          String firstName = FNameTxt.getText();
          String lastName = LNameTxt.getText();
          int ageMin = 0;
          int ageMax = 100;
          String city = CityTxt.getText();
          String personId = PIDTxt.getText();
          long contact = 0l;
          
           if(!AgeMinTxt.getText().trim().isEmpty()){
            try {
                ageMin = Integer.parseInt(AgeMinTxt.getText());
                } catch (NumberFormatException nfe) {
                     JOptionPane.showMessageDialog(this, "Age Min can only be numbers", "Error Message",JOptionPane.ERROR_MESSAGE);                
                     return;
                }
             }

            if(!AgeMaxTxt.getText().trim().isEmpty()){
             try {
                ageMax = Integer.parseInt(AgeMaxTxt.getText());
                } catch (NumberFormatException nfe) {
                     JOptionPane.showMessageDialog(this, "Age Max can only be numbers", "Error Message",JOptionPane.ERROR_MESSAGE);                
                     return;
                }
            }
            
            if(!AgeMinTxt.getText().trim().isEmpty() && !AgeMaxTxt.getText().trim().isEmpty()){
            if(Integer.parseInt(AgeMinTxt.getText()) >= Integer.parseInt(AgeMaxTxt.getText())){
                JOptionPane.showMessageDialog(this, "Seat Min Value should not be greater than or equal to Max Value", "Error Message",JOptionPane.ERROR_MESSAGE);                
                return;
            }
        }
        
        Predicate<Patient> fname = e -> (e.getFirstName().toLowerCase() == null ? firstName == null : e.getFirstName().toLowerCase().indexOf(firstName) >= 0);
        Predicate<Patient> lname = e -> (e.getLastName().toLowerCase() == null ? lastName == null : e.getLastName().toLowerCase().indexOf(lastName) >= 0);
        Predicate<Patient> cityName = e -> (e.getCity().toLowerCase() == null ? city == null : e.getCity().toLowerCase().indexOf(city) >= 0);
        Predicate<Patient> Id = e -> (e.getPersonId().toLowerCase() == null ? personId == null : e.getPersonId().toLowerCase().indexOf(personId) >= 0);
        Predicate<Patient> Males = e -> (e.getSex() == null && !Male.isSelected() ? null : e.getSex().equals("Male"));
        Predicate<Patient> Females = e -> (e.getSex() == null && !Female.isSelected() ? null : e.getSex().equals("Female"));
        Predicate<Patient> Test1s = e -> (e.getCommunity() == null && !Test1.isSelected() ? null : e.getCommunity().equals("Test1"));
        Predicate<Patient> Test2s = e -> (e.getCommunity() == null && !Test2.isSelected() ? null : e.getCommunity().equals("Test2"));
        Predicate<Patient> Test3s = e -> (e.getCommunity() == null && !Test3.isSelected() ? null : e.getCommunity().equals("Test3"));
        
         filterList = (ArrayList<Patient>) filterList.stream()
                .filter(fname.and(lname).and(cityName).and(Id))
                .collect(Collectors.toList());
        if(!AgeMinTxt.getText().trim().isEmpty() || !AgeMaxTxt.getText().trim().isEmpty()){
            final var minSeats = !AgeMinTxt.getText().trim().isEmpty()? Integer.parseInt(AgeMinTxt.getText()) : 2;
            final var maxSeats = !AgeMaxTxt.getText().trim().isEmpty() ? Integer.parseInt(AgeMaxTxt.getText()) : 100;
            Predicate<Person> Seat = e -> (minSeats <= e.getAge() && maxSeats >=e.getAge());
            filterList = (ArrayList<Patient>) filterList.stream()
                    .filter(Seat)
                    .collect(Collectors.toList());
        }
        
        if(Male.isSelected() && !Female.isSelected()){
             filterList = (ArrayList<Patient>) filterList.stream()
                    .filter(Males)
                    .collect(Collectors.toList());
        }
        if(Female.isSelected() && !Male.isSelected()){
             filterList = (ArrayList<Patient>) filterList.stream()
                    .filter(Females)
                    .collect(Collectors.toList());
        }
        if(Test1.isSelected() && !Test2.isSelected() && !Test3.isSelected()){
             filterList = (ArrayList<Patient>) filterList.stream()
                    .filter(Test1s)
                    .collect(Collectors.toList());
        }
        if(Test2.isSelected() && !Test1.isSelected() && !Test3.isSelected()){
             filterList = (ArrayList<Patient>) filterList.stream()
                    .filter(Test2s)
                    .collect(Collectors.toList());
        }
        if(Test3.isSelected() && !Test1.isSelected() && !Test2.isSelected()){
             filterList = (ArrayList<Patient>) filterList.stream()
                    .filter(Test3s)
                    .collect(Collectors.toList());
        }
      populateTable();
    }

    private void populateVitalTable(Patient selectedRow) {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        encounterList = selectedRow.getEncounterHistory().getEncounterHistory();
        for(Encounter cl: encounterList){
            Object[] row = new Object[10];
            row[0] = cl;
            row[1] = cl.getWardCategory();
            row[2] = cl.getClinicName();
            row[3] = selectedRow.getFirstName();
            row[4] = selectedRow.getLastName();
            row[5] = cl.getVital().getRespiratoryRate();
            row[6] = cl.getVital().getHeartRate();
            row[7] = cl.getVital().getBloodPressure();
            row[8] = cl.getVital().getWeight();
            row[9] = cl.getVital().getTime();
            model.addRow(row);
          } 
    }
